public class Circle {
    private double radius;
    private String colour;

    public Circle(double radius, String colour) {
        this.radius = radius;
        this.colour = colour;
    }
    public double getRadius() {
        return radius = 1.0;
    }
    public String getColour() {
        return colour = "Red";
    }

    public double getArea() {
           return Math.PI * (radius * radius);
    }

}
